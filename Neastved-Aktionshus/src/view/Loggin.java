    public void CreateBuyer() {
        String name;
        String address;
        int phoneNumber;
        try {

            name = jTextField1.getText();
            address = jTextField2.getText();
            phoneNumber = Integer.parseInt(jTextField3.getText());

            User u = new User(name, address, phoneNumber);
            userList.add(u);
            buyer = u;
            
            jComboBox1.removeAllItems();
            setCombobox();
            update(getGraphics());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Telefon nummeret kan ikke v√¶re tom eller bokstaver");
        }

    }

    public void getCombobox() {
        for (User user : userList) {
            if (user.getName().equals(jComboBox1.getSelectedItem())) {
                buyer = user;
            }
        }
    }

    public void login(User buyer) {
        
        MainFrame1 mf = new MainFrame1(auctionList, buyer);
        mf.setVisible(true);
        this.dispose();
    }
    
   

